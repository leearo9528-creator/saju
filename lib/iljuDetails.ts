/** goodMatch/badMatch 를 항상 배열로 반환 */
export function normalizeDetail(
  raw:
    | { characteristics: string[]; goodMatch: string | string[]; badMatch: string | string[] }
    | undefined
): {
  characteristics: string[];
  goodMatch: string[];
  badMatch: string[];
} | null {
  if (!raw) return null;
  return {
    characteristics: raw.characteristics,
    goodMatch: Array.isArray(raw.goodMatch) ? raw.goodMatch : [raw.goodMatch],
    badMatch: Array.isArray(raw.badMatch) ? raw.badMatch : [raw.badMatch],
  };
}
